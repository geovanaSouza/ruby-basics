# while loop
count = 1

while count < 10
    puts count
    count = count + 1
end

puts ""

# false loop
count = 6

until count > 10
    puts count
    count = count + 1
end

puts ""

# For loop
for count in 1...10
    puts count
end

puts ""

# For loop inclusive
for count in 1..10
    puts count
end